#!/usr/bin/env node
// Scrapes the H-group convention pages (hanabi.github.io/level-1 ... level-25)
// and writes a typed data module to src/lib/data/conventions.ts, used by the
// in-app conventions cheat-sheet panel.
//
// Each level becomes an entry with its conventions, where each convention's
// name and href come from an <h3> element on that level's page.
//
// Run with:  npm run scrape:conventions
//
// We read the rendered HTML rather than the raw .mdx source because the rendered
// page is the source of truth for which h3s exist (some are injected by MDX
// components) and for the exact anchor ids Docusaurus generates.

import { writeFile, mkdir } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const FIRST_LEVEL = 1;
const LAST_LEVEL = 25;
const BASE = "https://hanabi.github.io";

const OUT_DIR = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "lib", "data");
const OUT = join(OUT_DIR, "conventions.ts");

/** Decode the handful of HTML entities that show up in heading text. */
function decodeEntities(s) {
  return s
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&#x27;/g, "'");
}

/**
 * Pull the <h3> headings out of a level page's HTML.
 * Each h3 looks like: <h3 class="anchor ..." id=the-chop>The Chop<a href=#the-chop ...>
 * The id is unquoted; the visible text is everything up to the trailing hash-link <a>.
 */
function extractHeadings(html) {
  const headings = [];
  const h3 = /<h3\b[^>]*\bid=("?)([^"\s>]+)\1[^>]*>([\s\S]*?)<\/h3>/gi;
  let m;
  while ((m = h3.exec(html)) !== null) {
    const id = m[2];
    let inner = m[3];
    // Drop the trailing "Direct link" anchor and any other tags, keep text only.
    inner = inner.replace(/<a\b[^>]*>[\s\S]*?<\/a>/gi, "").replace(/<[^>]+>/g, "");
    const text = decodeEntities(inner).replace(/​/g, "").trim();
    if (text) headings.push({ name: text, id });
  }
  return headings;
}

async function fetchLevel(level) {
  const url = `${BASE}/level-${level}`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`level-${level}: HTTP ${res.status}`);
  const html = await res.text();
  const headings = extractHeadings(html);
  if (headings.length === 0) throw new Error(`level-${level}: no <h3> headings found`);
  return { level, url, headings };
}

async function main() {
  const levels = [];
  for (let level = FIRST_LEVEL; level <= LAST_LEVEL; level++) {
    const { url, headings } = await fetchLevel(level);
    process.stderr.write(`level-${level}: ${headings.length} headings\n`);
    levels.push({
      level,
      conventions: headings.map((h) => ({ name: h.name, href: `${url}#${h.id}` })),
    });
  }

  const ts = `// AUTO-GENERATED by scripts/scrape-conventions.mjs — do not edit by hand.
// Run \`npm run scrape:conventions\` to refresh from ${BASE}.

export interface Convention {
  name: string;
  href: string;
}

export interface LevelConventions {
  level: number;
  conventions: Convention[];
}

export const conventionsByLevel: LevelConventions[] = ${JSON.stringify(levels, null, 2)};
`;

  await mkdir(OUT_DIR, { recursive: true });
  await writeFile(OUT, ts);
  process.stderr.write(`\nWrote ${OUT}\n`);
}

main().catch((err) => {
  process.stderr.write(`Error: ${err.message}\n`);
  process.exit(1);
});
